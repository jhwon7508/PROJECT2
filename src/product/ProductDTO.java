package product;

public class ProductDTO {

	private int proId;
	private String proName;
	private int proPrice;
	private String proImg;
	private String proImg2;
	private String proExp;
	private String proSort;
	
	public int getProId() {
		return proId;
	}
	public void setProId(int proId) {
		this.proId = proId;
	}
	public String getProName() {
		return proName;
	}
	public void setProName(String proName) {
		this.proName = proName;
	}
	public int getProPrice() {
		return proPrice;
	}
	public void setProPrice(int proPrice) {
		this.proPrice = proPrice;
	}
	public String getProImg() {
		return proImg;
	}
	public void setProImg(String proImg) {
		this.proImg = proImg;
	}
	public String getProImg2() {
		return proImg2;
	}
	public void setProImg2(String proImg2) {
		this.proImg2 = proImg2;
	}
	public String getProExp() {
		return proExp;
	}
	public void setProExp(String proExp) {
		this.proExp = proExp;
	}
	public String getProSort() {
		return proSort;
	}
	public void setProSort(String proSort) {
		this.proSort = proSort;
	}
	
	
	
	
}
